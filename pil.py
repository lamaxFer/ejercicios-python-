# Inicializamos la pila con algunos elementos
pila = [3, 4, 5]

# Agregamos nuevos elementos a la pila
pila.append(6)  # Añadimos 6
pila.append(7)  # Añadimos 7

# Mostramos el estado de la pila después de las operaciones
print(pila)  # Salida: [3, 4, 5, 6, 7]

# Agregamos una variable
nuevo_elemento = 8
pila.append(nuevo_elemento)  # Añadimos la variable a la pila

# Mostramos el estado final de la pila
print(pila)  # Salida: [3, 4, 5, 6, 7, 8]
